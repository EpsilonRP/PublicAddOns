local function registerNewString(EN, FR, ES, DE, PT, RU)
	if not EN then return end
	LangEN[EN] = EN
	if FR then LangFR[EN] = FR end
	if ES then LangES[EN] = ES end
	if DE then LangDE[EN] = DE end
	if PT then LangPT[EN] = PT end
	if RU then LangRU[EN] = RU end
end

local langMap = {
	English = LangEN,
	French = LangFR,
	German = LangDE,
	Spanish = LangES,
	Portuguese = LangPT,
	Russian = LangRU
}

local langCodeMap = {
	EN = LangEN,
	FR = LangFR,
	DE = LangDE,
	ES = LangES,
	PT = LangPT,
	RU = LangRU
}

local langCodeToName = {
	EN = "English",
	FR = "French",
	ES = "Spanish",
	DE = "German",
	PT = "Portuguese",
	RU = "Russian"
}

local function getLangNameByCode(code)
	return langCodeToName[code] or langCodeToName['EN']
end
ns.getLangNameByCode = getLangNameByCode

local function getLangTabByString(_langSelected)
	--[[
	if _langSelected == "English" then
		return LangEN;
	elseif _langSelected == "French" then
		return LangFR
	elseif _langSelected == "Spanish" then
		return LangES
	elseif _langSelected == "German" then
		return LangDE
	elseif _langSelected == "Portuguese" then
		return LangPT
	elseif _langSelected == "Russian" then
		return LangRU

	end
	--]]
	-- returns the language based on the map, or code, or English if lang not found (how?)
	return ((langMap[_langSelected] or langCodeMap[_langSelected]) or langMap['English'])
end
ns.getLangTabByString = getLangTabByString

local function FindStringId(LangList, searchString)
	for id, value in ipairs(LangList) do
		if value == searchString then
			return id
		end
	end
	return nil
end

function PhaseToolkitPanel.getBaseLang()
	return "English", LangEN;
end

function PhaseToolkitPanel.createConfigPanel()
	PhaseToolkitPanel.panel = CreateFrame("Frame", addonName .. "OptionsPanel", UIParent);
	PhaseToolkitPanel.panel.name = addonName;
	InterfaceOptions_AddCategory(PhaseToolkitPanel.panel);

	local title = PhaseToolkitPanel.panel:CreateFontString(nil, "OVERLAY", "GameFontNormalLarge");
	title:SetPoint("TOPLEFT", 16, -16);
	title:SetText("Phase toolkit Settings");


	local subText = PhaseToolkitPanel.panel:CreateFontString(nil, "OVERLAY", "GameFontHighlightSmall");
	subText:SetPoint("TOPLEFT", title, "BOTTOMLEFT", 0, -8);
	subText:SetText("Language selection");

	local function ShowLangDropdown(DropDown)
		local function OnClick(self)
			UIDropDownMenu_SetSelectedValue(DropDown, self.value)
			PhaseToolkitPanel.selectedLang = self.value
			PhaseToolKitConfig["CurrentLang"] = self.value -- Save just the name of the language, then we will pull it in changeLang and use that instead
			PhaseToolkit.changeLang(PhaseToolKitConfig["CurrentLang"])
		end

		UIDropDownMenu_Initialize(DropDown, function()
			for i = 1, #PhaseToolkitPanel.LangList do
				local info = UIDropDownMenu_CreateInfo()
				info.value = PhaseToolkitPanel.LangList[i]
				info.text = PhaseToolkitPanel.LangList[i]

				info.func = OnClick
				UIDropDownMenu_AddButton(info)
			end
		end)


		UIDropDownMenu_SetWidth(DropDown, 120)
		UIDropDownMenu_SetButtonWidth(DropDown, 124)

		if (PhaseToolKitConfig["CurrentLang"] == nil) then
			UIDropDownMenu_SetSelectedValue(DropDown, "English");
			PhaseToolKitConfig["CurrentLang"] = "English"
			PhaseToolkit.changeLang("English")
		else
			UIDropDownMenu_SetSelectedValue(DropDown, PhaseToolKitConfig["CurrentLang"])
			--PhaseToolkit.changeLang(PhaseToolKitConfig["CurrentLang"]) -- We don't need to changeLang here?
		end
	end

	local LangConfigDropDown = CreateFrame("Frame", "LangConfigDropDown", PhaseToolkitPanel.panel, "UIDropDownMenuTemplate");
	LangConfigDropDown:SetSize(200, 30);
	LangConfigDropDown:SetPoint("TOPLEFT", subText, "BOTTOMLEFT", 0, -10);

	ShowLangDropdown(LangConfigDropDown);
end
